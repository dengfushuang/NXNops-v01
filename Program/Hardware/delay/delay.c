#include "delay.h"
/*************************************************************************************************************
*函数名称：delay_nms()
*参   数 ：uint16_t n   延时N个毫秒
*返回值  : void
*功能描述：软件延时N个毫秒，外部调用
**************************************************************************************************************/
void delay_nms(uint16_t n) 
{
	uint16_t  a,b;
	for (a=0;a<n;a++)
	{
		for (b=0;b<9000;b++);
	}
}


/*******************************************************************************************************
** 函数名称: DelayNms
** 功能描述: 延时N ms
** 输　入: pdata        任务附加参数(指针)
** 输　出: 无
********************************************************************************************************/
void DelayNms(uint8_t delx)
{
    uint16_t  i,j,k=0;
    while(k<delx)
    {
        k++;
        for(i = 0; i <= 2200*2; i ++)
        {
            j ++;
            j --;
        }
    }
}
