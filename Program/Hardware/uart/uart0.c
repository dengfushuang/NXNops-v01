uint8 UART0Getch(void)
{
    uint8 err;
	//OS_CPU_SR  cpu_sr;
	
    //OS_ENTER_CRITICAL(;
    while ((LPC_UART0->LSR & 0x00000001) == 0)
    {                                           /* 没有收到数据 */
        LPC_UART0->IER = LPC_UART0->IER | 0x01; /* 允许接收中断 */
        OSSemPend(Uart0Sem, 0, &err);           /* 等待接收数据 */
    }
    err = LPC_UART0->RBR;                       /* 读取收到的数据 */
    //OS_EXIT_CRITICAL(;
    return err;
}

/*********************************************************************************************************
** 函数名称: UART0_Exception
** 功能描述: UART0中断服务程序
** 输　入: 无
**
** 输　出: 无
**
** 全局变量: 无
** 调用模块: QueueRead,OSSemPost
**
** 作　者: 陈明计
** 日　期: 2003年7月4日
**-------------------------------------------------------------------------------------------------------
** 修改人: 陈明计
** 日　期: 2003年7月21日
**------------------------------------------------------------------------------------------------------
********************************************************************************************************/
void UART0_IRQHandler (void) 
{
    uint8 IIR, temp, i;
	  OS_CPU_SR  cpu_sr;
	
    OS_ENTER_CRITICAL();

    while(((IIR = LPC_UART0->IIR) & 0x01) == 0)
    {                                                   /* 有中断未处理完 */
        switch (IIR & 0x0e)
        {
            case 0x02:                                  /* THRE中断    */
                for (i = 0; i < UART0_FIFO_LENGTH; i++) /* 向发送FIFO填充数据 */
                {
                    if (QueueRead(&temp, UART0SendBuf) == QUEUE_OK)
                    {
                        LPC_UART0->THR = temp;
                    }
                    else
                    {
                        LPC_UART0->IER = LPC_UART0->IER & (~0x02);        /* 队列空，则禁止发送中断 */
                    }
                }
                break;
            case 0x04:                                  /* 接收数据可用 */
                OSSemPost(Uart0Sem);                    /* 通知接收任务 */
                LPC_UART0->IER = LPC_UART0->IER & (~0x01);                /* 禁止接收及字符超时中断 */
                break;
            case 0x06:                                  /* 接收线状态   */
                temp = LPC_UART0->LSR;
                break;
            case 0x0c:                                  /* 字符超时指示 */
                OSSemPost(Uart0Sem);                    /* 通知接收任务 */
                LPC_UART0->IER = LPC_UART0->IER & (~0x01);                /* 禁止接收及字符超时中断 */
                break;
            default :
            	temp = LPC_UART0->LSR;
                break;
        }
    }
    //VICVectAddr = 0;            // 通知中断控制器中断结束
    OS_EXIT_CRITICAL();
}

