extern uint32 SystemCoreClock;
uint8  UART0Init(void)
{
    uint16 Fdiv;
    uint32 bps; 
    //使能以UART0控制器电源    
    LPC_SC->PCONP |= (1<<3);
    // Enable UART0 Pins.
    LPC_IOCON->P0_2 = 0x01;     //UART0_TXD
    LPC_IOCON->P0_3 = 0x01;     //UART0_RXD
	
    switch ( EPROM.BPS )
    {
        case 1 :
             bps = 2400;
            break;
        case 2 :
             bps = 4800;
            break;
        case 3 :
             bps = 9600;
            break;
        case 4 :
             bps = 14400;
            break;
        case 5 :
             bps = 19200;
            break;
        case 6 :
             bps = 38400;
            break;
        case 7 :
             bps = 56000;
            break;
        case 8 :
             bps = 57600;
            break;
         case 9 :
             bps = 115200;
            break;               
        default:  break;
    }
    LPC_UART0->LCR = 0x80;                      // 允许访问分频因子寄存器     
    Fdiv = (PeripheralClock / 16) / bps;        // 设置波特率 
    LPC_UART0->DLM = Fdiv / 256;
	  LPC_UART0->DLL = Fdiv % 256;
    LPC_UART0->LCR = 0x03;                      // 禁止访问分频因子寄存器 
                                                // 且设置为8,1,n 
	  LPC_UART0->IER = 0x05;                      // 允许接收和发送中断 
    LPC_UART0->FCR = 0x87;                      // 初始化FIFO 

    NVIC_EnableIRQ(UART0_IRQn);                 // Enable UART0 interrupt 
    
    if (QueueCreate((void *)UART0SendBuf,
                     sizeof(UART0SendBuf),
                     NULL,
                     (uint8 (*)())Uart0WriteFull) 
                     == NOT_OK)
    {
        return FALSE;
    }
    Uart0Sem = OSSemCreate(0);
    if (Uart0Sem != NULL)
    {
        return TRUE;
    }
    else
    {
        return FALSE;
    }
}

