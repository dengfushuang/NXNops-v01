 void UART0Put_str(uint8 *Data, uint16 NByte)
{
    //OS_ENTER_CRITICAL(;
    while (NByte-- > 0)
    {
        UART0Putch(*Data++);
    }
    //OS_EXIT_CRITICAL(;
}

void UART0Write_Str(uint8 *Data)
{
    //OS_ENTER_CRITICAL(;
    while (*Data != '\0' )
    {
        UART0Putch(*Data++);
    }
    //OS_EXIT_CRITICAL(;
}
